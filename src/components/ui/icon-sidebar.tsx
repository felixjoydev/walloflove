"use client";

import Link from "next/link";
import { useParams, usePathname } from "next/navigation";

const navItems = [
  { icon: InboxIcon, tooltip: "Inbox", segment: "inbox" },
  { icon: GlobeIcon, tooltip: "Publish", segment: "preview" },
  { icon: ChartIcon, tooltip: "Analytics", segment: "analytics" },
  { icon: GearIcon, tooltip: "Settings", segment: "settings" },
];

export function IconSidebar() {
  const params = useParams();
  const pathname = usePathname();
  const guestbookId = params.id as string | undefined;

  if (!guestbookId) return null;

  return (
    <aside className="flex w-[60px] shrink-0 flex-col items-center border-r border-neutral-800 bg-neutral-950 py-4">
      <nav className="flex flex-1 flex-col gap-1">
        {navItems.map((item) => {
          const href = `/guestbooks/${guestbookId}/${item.segment}`;
          const isActive = pathname.includes(`/${item.segment}`);

          return (
            <Link
              key={item.segment}
              href={href}
              title={item.tooltip}
              className={`flex h-10 w-10 items-center justify-center rounded-lg transition-colors ${
                isActive
                  ? "bg-neutral-800 text-white"
                  : "text-neutral-400 hover:bg-neutral-800/50 hover:text-white"
              }`}
            >
              <item.icon />
            </Link>
          );
        })}
      </nav>
    </aside>
  );
}

